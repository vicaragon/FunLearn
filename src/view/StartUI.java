    private void startPlayingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startPlayingActionPerformed
        switch (SetupUI.activityType) {
            case 1:
                GameUI gameUI = new GameUI(SetupUI.activityLevel,StudentsListUI.getPlayerController().getPlayerTablemodel().getPlayerIDs(),StudentsListUI.getPlayerController().getPlayerTablemodel().getPlayerList());
                SetupUI.getCardLayout().show(SetupUI.getCards(),"StudentList");
                gameUI.setVisible(true);
                gameUI.start();
                break;
            case 2:
                StoryUI storyUI = new StoryUI(SetupUI.activityLevel);
                SetupUI.getCardLayout().show(SetupUI.getCards(),"StudentList");
                storyUI.setVisible(true);
                storyUI.start();
                break;
            case 3:
                SongUI songUI = new SongUI(SetupUI.activityLevel);
                SetupUI.getCardLayout().show(SetupUI.getCards(),"StudentList");
                songUI.setVisible(true);
                songUI.start();
                break;
            default:
                SongUI songDef = new SongUI(SetupUI.activityLevel);
                SetupUI.getCardLayout().show(SetupUI.getCards(),"StudentList");
                songDef.setVisible(true);
                songDef.start();
        }
    }//GEN-LAST:event_startPlayingActionPerformed

