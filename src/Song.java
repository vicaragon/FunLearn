
class Song{
	private String songName;
	private int ageGroup;
	private int level;
	
	public String getName(){
		return songName;
	}
	
	public void setName(String name){
		songName = name;
	}
	
	public int getAge(){
		return ageGroup;
	}
	
	public void setAge(int age){
		ageGroup = age;
	}
	
	public int getLevel(){
		return level;
	}
	
	public void setLevel(int l){
		level = l;
	}
		
}